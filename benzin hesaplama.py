#PROBLEM 3(Bir aracın kilometrede ne kadar yaktığı ve kaç kilometre yol yaptığı bilgilerini alın ve sürücünü toplam ne kadar ödemesini gerektiğini hesaplayın.)
a=int(input("aracın kilometre başı yaktığı kuruş:"))
b=int(input("aracın kaç kilometre yaptı:"))
c=a*b

print("araç kilometre başına {} kuruş yakarsa {} kilometrede {} kuruş ödememe yaparsın.".format(a,b,c))
